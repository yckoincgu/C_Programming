#include <stdio.h>

int main() {
    int number;

  
    printf("輸入一個整數: ");
    scanf("%d", &number);

 
    switch (number) {
        case 20:
            printf("輸入的數字是 20。\n");
            break;
        case 21:
            printf("輸入的數字是 21。\n");
            break;
        case 22:
            printf("輸入的數字是 22。\n");
            break;
        case 23:
            printf("輸入的數字是 23。\n");
            break;
        case 24:
            printf("輸入的數字是 24。\n");
            break;
        default:
            printf("輸入的數字不是 20、21、22、23 或 24。\n");
            break;
    }

    return 0;
}








	
	
	
	
	
	 